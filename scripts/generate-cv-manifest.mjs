import { mkdir, readdir, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const rootDir = dirname(dirname(fileURLToPath(import.meta.url)))
const cvDir = join(rootDir, 'public', 'cv')
const outputPath = join(rootDir, 'src', 'generated', 'cv-manifest.ts')

const rules = {
  zh: {
    label: 'Chinese CV',
    pattern: /简历-(\d{6})\.pdf$/u,
  },
  en: {
    label: 'English CV',
    pattern: /CV-(\d{6})\.pdf$/iu,
  },
}

const formatDate = (code) => {
  const year = 2000 + Number(code.slice(0, 2))
  const month = Number(code.slice(2, 4))
  const day = Number(code.slice(4, 6))
  const date = new Date(Date.UTC(year, month - 1, day))

  if (
    date.getUTCFullYear() !== year ||
    date.getUTCMonth() !== month - 1 ||
    date.getUTCDate() !== day
  ) {
    throw new Error(`Invalid CV date code: ${code}`)
  }

  return `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`
}

const files = await readdir(cvDir)
const pdfs = files.filter((file) => file.toLowerCase().endsWith('.pdf'))
const matches = { zh: [], en: [] }
const unmatched = []

for (const filename of pdfs) {
  let matched = false

  for (const [lang, rule] of Object.entries(rules)) {
    const match = filename.match(rule.pattern)
    if (!match) continue

    const dateCode = match[1]
    matches[lang].push({
      lang,
      label: rule.label,
      filename,
      href: `/cv/${encodeURIComponent(filename)}`,
      dateCode,
      updated: formatDate(dateCode),
    })
    matched = true
  }

  if (!matched) unmatched.push(filename)
}

for (const filename of unmatched) {
  console.warn(`[cv-manifest] ignored unmatched PDF: ${filename}`)
}

const latestEntries = Object.entries(matches).map(([lang, entries]) => {
  if (entries.length === 0) {
    throw new Error(`[cv-manifest] missing ${lang} CV in public/cv`)
  }

  return entries.sort((a, b) => b.dateCode.localeCompare(a.dateCode))[0]
})

const output = `// This file is generated by scripts/generate-cv-manifest.mjs.
// Do not edit it by hand. Put CV PDFs in public/cv/ and rerun npm scripts.

export type CvLanguage = 'zh' | 'en'

export interface CvEntry {
  lang: CvLanguage
  label: string
  filename: string
  href: string
  dateCode: string
  updated: string
}

export const cvEntries: CvEntry[] = ${JSON.stringify(latestEntries, null, 2)}
`

await mkdir(dirname(outputPath), { recursive: true })
await writeFile(outputPath, output)
console.log(`[cv-manifest] wrote ${latestEntries.length} entries to src/generated/cv-manifest.ts`)
